/**
 * Names and initals programing challenge
 */

public class NamesAndInitals
{
    public static void main(String[] args)
    {
        String firstName = "Cody", middleName = "James", lastName = "Hill";
        char firstInital = 'C', middleInitial = 'J', lastInitial = 'H';
        System.out.printf("%s %s %s %s %s %s\n", firstName, middleName, lastName, firstInital, middleInitial, lastInitial);
    }
}